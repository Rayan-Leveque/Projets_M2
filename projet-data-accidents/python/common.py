def normaliser_dep(dep_brut: str) -> str | None:
    if dep_brut is None:
        return None
    s = dep_brut.strip()
    if not s.isdigit():
        return None
    if s in DEP_DOM:
        return None
    if s == "201":
        return "2A"
    if s == "202":
        return "2B"
    n = int(s)
    if n >= 10 and n % 10 == 0 and 1 <= n // 10 <= 95:
        return f"{n // 10:02d}"
    return None


def charger_num_acc_metropole(year: int) -> set[str]:
    path = ENRICHED / f"caracteristiques_{year}.csv"
    metropole: set[str] = set()
    with path.open("r", encoding="utf-8", newline="") as f:
        reader = csv.reader(f)
        header = next(reader)
        idx_num = header.index("Num_Acc")
        idx_dep = header.index("dep")
        for row in reader:
            if normaliser_dep(row[idx_dep]) is not None:
                metropole.add(row[idx_num])
    return metropole


